package de.minee.datamodel;

import java.util.UUID;

public class PrimitiveObjects {

	private UUID id;
	private Long longValue;
	private Integer intValue;
	private Short shortValue;
	private Byte byteValue;
	private Boolean boolValue;
	private Character charValue;
	private Double doubleValue;
	private Float floatValue;
	public UUID getId() {
		return id;
	}
	public void setId(final UUID id) {
		this.id = id;
	}
	public Long getLongValue() {
		return longValue;
	}
	public void setLongValue(final Long longValue) {
		this.longValue = longValue;
	}
	public Integer getIntValue() {
		return intValue;
	}
	public void setIntValue(final Integer intValue) {
		this.intValue = intValue;
	}
	public Short getShortValue() {
		return shortValue;
	}
	public void setShortValue(final Short shortValue) {
		this.shortValue = shortValue;
	}
	public Byte getByteValue() {
		return byteValue;
	}
	public void setByteValue(final Byte byteValue) {
		this.byteValue = byteValue;
	}
	public Boolean getBoolValue() {
		return boolValue;
	}
	public void setBoolValue(final Boolean boolValue) {
		this.boolValue = boolValue;
	}
	public Character getCharValue() {
		return charValue;
	}
	public void setCharValue(final Character charValue) {
		this.charValue = charValue;
	}
	public Double getDoubleValue() {
		return doubleValue;
	}
	public void setDoubleValue(final Double doubleValue) {
		this.doubleValue = doubleValue;
	}
	public Float getFloatValue() {
		return floatValue;
	}
	public void setFloatValue(final Float floatValue) {
		this.floatValue = floatValue;
	}

}
